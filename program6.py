#AVERAGE TEMPRATURE OF WEEK
a=float(input("Enter temprature of Monday:"))
b=float(input("Enter temprature of Tuesday:"))
c=float(input("Enter temprature of Wednesday:"))
d=float(input("Enter temprature of Thursday:"))
e=float(input("Enter temprature of Friday:"))
f=float(input("Enter temprature of Saturday:"))
g=float(input("Enter temprature of Sunday:"))
x=(a+b+c+d+e+f+g)/7
z=round(x,2)
print("-------------------------------------------")
print("AVERAGE TEMPRATURE OF WEEK:",z,"celcius")
      
